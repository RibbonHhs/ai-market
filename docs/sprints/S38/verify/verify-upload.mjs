#!/usr/bin/env node
// verify-upload.mjs
// S38 E2E verify: 后端起 → 前端 build → 登录 → /admin/skills/new → 上传 → 跳详情 → 列表可搜 → 下载字节一致
//
// 用法:
//   1) 依赖准备（一次）: cd frontend && npm install
//   2) 启动脚本: node docs/sprints/S38/verify/verify-upload.mjs
//   3) 退出码 0=PASS, 1=FAIL
//
// 依赖: playwright（devDependency），已存在则直接用；不存在则提示 Lead 装依赖，脚本仍可写出但运行失败
//
// 退出码:
//   0 - 所有 E2E 断言通过
//   1 - 任何阶段失败（后端启动 / 前端 build / Playwright / sha256）

import { chromium } from 'playwright'
import { spawn, execSync, spawnSync } from 'child_process'
import { existsSync, readFileSync, writeFileSync, mkdirSync, statSync, createHash } from 'fs'
import { createRequire } from 'module'
import path from 'path'
import { fileURLToPath } from 'url'
import http from 'http'

const require = createRequire(import.meta.url)
const __dirname = path.dirname(fileURLToPath(import.meta.url))
const PROJECT_ROOT = path.resolve(__dirname, '../../../..')        // D:\codeing\workspace\skills-map
const BACKEND_DIR = path.join(PROJECT_ROOT, 'backend')
const FRONTEND_DIR = path.join(PROJECT_ROOT, 'frontend')
const SCREENSHOTS_DIR = path.join(__dirname, '..', 'screenshots')
const TMP_DIR = path.join(__dirname, '.tmp')

// ====== 路径常量 ======
const BACKEND_PORT = 8767
const FRONTEND_PORT = 7777
const BACKEND_BASE = `http://127.0.0.1:${BACKEND_PORT}`
const FRONTEND_BASE = `http://127.0.0.1:${FRONTEND_PORT}`

// 测试账号（dev 模式 H2 默认种子用户，role=USER）
const TEST_USER = { username: 'tester', password: 'tester123' }

// ====== 工具：彩色日志 ======
const C = {
  reset: '\x1b[0m', green: '\x1b[32m', red: '\x1b[31m', yellow: '\x1b[33m', blue: '\x1b[34m', dim: '\x1b[2m'
}
function log(stage, msg, color = 'reset') {
  console.log(`${C[color]}[${stage}]${C.reset} ${msg}`)
}
function fail(stage, msg) { log(stage, msg, 'red'); throw new Error(msg) }

// ====== 0. 准备 screenshots 目录 ======
mkdirSync(SCREENSHOTS_DIR, { recursive: true })
mkdirSync(TMP_DIR, { recursive: true })

// ====== 1. 准备 sample zip（含 SKILL.md + frontmatter + 1 资源） ======
function makeSampleZip() {
  const out = path.join(TMP_DIR, 'sample-skill.zip')
  if (existsSync(out)) return out

  // 用 PowerShell 的 Compress-Archive 打包（Windows 环境已自带）
  const skillDir = path.join(TMP_DIR, 'sample-skill')
  mkdirSync(path.join(skillDir, 'scripts'), { recursive: true })
  writeFileSync(path.join(skillDir, 'SKILL.md'),
    '---\n' +
    'name: verify-sample-skill\n' +
    'description: Generated by verify-upload.mjs (S38 E2E)\n' +
    'version: 1.0.0\n' +
    'author: qa-tina\n' +
    '---\n\n' +
    '# verify-sample-skill\n\nA tiny skill used by the E2E verify script.\n')
  writeFileSync(path.join(skillDir, 'scripts', 'hello.sh'), '#!/usr/bin/env bash\necho hello from verify-sample-skill\n')

  // 删旧 zip 防止 Append
  if (existsSync(out)) {
    spawnSync('cmd', ['/c', 'del', '/q', out], { stdio: 'ignore' })
  }
  execSync(
    `powershell -NoProfile -Command "Compress-Archive -Path '${skillDir}\\*' -DestinationPath '${out}' -Force"`,
    { stdio: 'ignore' }
  )
  if (!existsSync(out)) fail('ZIP', 'Compress-Archive failed to produce zip')
  const size = statSync(out).size
  log('ZIP', `生成 ${out} (${size} bytes)`, 'green')
  return out
}

// ====== 2. 启动后端（H2 dev 模式，无需 MySQL） ======
function startBackend() {
  log('BACKEND', `启动 Spring Boot (port=${BACKEND_PORT})...`, 'blue')
  // 优先 mvnw，回退 mvn
  const cmd = process.platform === 'win32' ? 'mvnw.cmd' : './mvnw'
  const cmdPath = path.join(BACKEND_DIR, process.platform === 'win32' ? 'mvnw.cmd' : 'mvnw')
  if (!existsSync(cmdPath)) {
    log('BACKEND', 'mvnw 不存在，回退到 mvn（需系统装 Maven 3.9+）', 'yellow')
  }
  const proc = spawn(
    existsSync(cmdPath) ? cmdPath : 'mvn',
    ['spring-boot:run', '-Dspring-boot.run.profiles=dev', '-Dspring-boot.run.jvmArguments=-Xmx512m'],
    { cwd: BACKEND_DIR, shell: true, stdio: ['ignore', 'pipe', 'pipe'] }
  )

  let stderrBuf = ''
  proc.stderr.on('data', d => { stderrBuf += d.toString() })

  return new Promise((resolve, reject) => {
    const timer = setTimeout(() => {
      proc.kill()
      reject(new Error(`后端启动超时（90s）。stderr 末段:\n${stderrBuf.slice(-2000)}`))
    }, 90_000)

    const poll = setInterval(async () => {
      try {
        const ok = await httpProbe(`${BACKEND_BASE}/actuator/health`, 2000)
        if (ok) {
          clearInterval(poll); clearTimeout(timer)
          log('BACKEND', '健康检查通过', 'green')
          resolve(proc)
        }
      } catch { /* retry */ }
    }, 1500)
  })
}

function httpProbe(url, timeoutMs = 3000) {
  return new Promise((resolve, reject) => {
    const req = http.get(url, res => { res.resume(); resolve(res.statusCode === 200) })
    req.on('error', reject)
    req.setTimeout(timeoutMs, () => req.destroy(new Error('timeout')))
  })
}

// ====== 3. 启动前端（Vite dev server，Playwright 直接用） ======
function startFrontend() {
  log('FRONTEND', `启动 Vite dev server (port=${FRONTEND_PORT})...`, 'blue')
  if (!existsSync(path.join(FRONTEND_DIR, 'node_modules'))) {
    fail('FRONTEND', 'frontend/node_modules 不存在。请先 `cd frontend && npm install`（脚本不自动 install，避免改 lockfile）')
  }
  const proc = spawn(
    process.platform === 'win32' ? 'npm.cmd' : 'npm',
    ['run', 'dev', '--', '--port', String(FRONTEND_PORT), '--strictPort'],
    { cwd: FRONTEND_DIR, shell: true, stdio: ['ignore', 'pipe', 'pipe'], env: { ...process.env, BROWSER: 'none' } }
  )
  let stderrBuf = ''
  proc.stderr.on('data', d => { stderrBuf += d.toString() })

  return new Promise((resolve, reject) => {
    const timer = setTimeout(() => {
      proc.kill()
      reject(new Error(`前端启动超时（60s）。stderr 末段:\n${stderrBuf.slice(-2000)}`))
    }, 60_000)

    const poll = setInterval(async () => {
      try {
        const ok = await httpProbe(`${FRONTEND_BASE}/`, 2000)
        if (ok) {
          clearInterval(poll); clearTimeout(timer)
          log('FRONTEND', '首页响应 200', 'green')
          resolve(proc)
        }
      } catch { /* retry */ }
    }, 1500)
  })
}

// ====== 4. 拿 token（直接调 /api/auth/login，绕过 UI） ======
async function loginViaApi() {
  log('AUTH', `登录 ${TEST_USER.username}...`, 'blue')
  const body = new URLSearchParams(TEST_USER).toString()
  const res = await fetch(`${BACKEND_BASE}/api/auth/login`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
    body
  })
  if (!res.ok) fail('AUTH', `登录失败: HTTP ${res.status}`)
  const json = await res.json()
  const token = json?.data?.token ?? json?.data?.accessToken
  if (!token) fail('AUTH', `响应无 token: ${JSON.stringify(json).slice(0, 200)}`)
  log('AUTH', `拿到 token（前 20 字符）: ${token.slice(0, 20)}...`, 'green')
  return token
}

// ====== 5. Playwright E2E ======
async function runE2E(token) {
  log('E2E', '启动 Chromium...', 'blue')
  const browser = await chromium.launch({ headless: true })
  const ctx = await browser.newContext({
    viewport: { width: 1440, height: 900 },
    // 预置 LocalStorage 跳过登录 UI（等价于已登录）
    storageState: {
      cookies: [],
      origins: [{
        origin: FRONTEND_BASE,
        localStorage: [{ name: 'auth_token', value: token }],
      }],
    },
  })
  const page = await ctx.newPage()

  try {
    // ---- 步骤 1: 进 /admin/skills/new（已登录，应直达上传页）----
    log('E2E', 'Step 1: GET /admin/skills/new', 'blue')
    await page.goto(`${FRONTEND_BASE}/admin/skills/new`, { waitUntil: 'networkidle' })
    await page.waitForSelector('[data-testid="upload-dragger"]', { timeout: 10000 })
    log('E2E', '上传页可见', 'green')

    // ---- 步骤 2: 空表单提交按钮 disabled（断言 B / canSubmit）----
    log('E2E', 'Step 2: 提交按钮 disabled（空表单）', 'blue')
    const disabledBefore = await page.locator('[data-testid="submit-upload"]').isDisabled()
    if (!disabledBefore) fail('E2E', '空表单提交按钮未禁用')
    log('E2E', 'disabled=true 断言通过', 'green')

    // ---- 步骤 3: 拖入 zip（用 setInputFiles 模拟）----
    log('E2E', 'Step 3: setInputFiles 拖入 sample.zip', 'blue')
    const zipPath = makeSampleZip()
    await page.setInputFiles('[data-testid="upload-dragger"] input[type="file"]', zipPath)
    await page.waitForTimeout(500)
    const fileName = await page.locator('.ant-upload-drag-hint').first().innerText()
    if (!fileName.includes('sample-skill.zip')) fail('E2E', `dragger 未识别文件名: "${fileName}"`)
    log('E2E', `dragger 已显示: ${fileName}`, 'green')

    // ---- 步骤 4: 选 SOC 分类 ----
    log('E2E', 'Step 4: 选 SOC 分类', 'blue')
    // 等 a-cascader 加载完毕（change-on-select）
    await page.waitForSelector('[data-testid="cascader-soc"]', { timeout: 5000 })
    await page.locator('[data-testid="cascader-soc"]').click()
    await page.waitForTimeout(300)
    // 选第一个一级菜单的第一项
    await page.locator('.ant-cascader-menu').first().locator('.ant-cascader-menu-item').first().click()
    await page.waitForTimeout(300)
    // 选第一个二级菜单的第一项
    const subMenuVisible = await page.locator('.ant-cascader-menu').nth(1).isVisible().catch(() => false)
    if (subMenuVisible) {
      await page.locator('.ant-cascader-menu').nth(1).locator('.ant-cascader-menu-item').first().click()
    }
    await page.waitForTimeout(300)
    // 关掉 cascader 浮层（点页面空白处）
    await page.keyboard.press('Escape')
    log('E2E', 'SOC 已选', 'green')

    // ---- 步骤 5: 点提交 ----
    log('E2E', 'Step 5: 点提交', 'blue')
    await page.screenshot({ path: path.join(SCREENSHOTS_DIR, '00-before-submit.png') })
    const submitBtn = page.locator('[data-testid="submit-upload"]')
    await submitBtn.waitFor({ state: 'visible' })
    // canSubmit 应为 true
    if (await submitBtn.isDisabled()) fail('E2E', '提交按钮仍 disabled（file/categoryId 未齐）')

    // ---- 步骤 6: 等跳详情（toast + setTimeout(1500) 后 push）----
    log('E2E', 'Step 6: 等跳详情页 /skills/verify-sample-skill', 'blue')
    await Promise.all([
      page.waitForURL(/\/skills\/verify-sample-skill/, { timeout: 15000 }),
      submitBtn.click(),
    ])
    log('E2E', `跳到: ${page.url()}`, 'green')
    await page.screenshot({ path: path.join(SCREENSHOTS_DIR, '01-upload-success.png') })

    // ---- 步骤 7: 列表可搜 ----
    log('E2E', 'Step 7: GET /api/skills?keyword=verify-sample-skill 验证落库', 'blue')
    const searchRes = await fetch(`${BACKEND_BASE}/api/skills?keyword=verify-sample-skill`, {
      headers: { Authorization: `Bearer ${token}` }
    })
    if (!searchRes.ok) fail('E2E', `搜索失败 HTTP ${searchRes.status}`)
    const searchJson = await searchRes.json()
    const list = searchJson?.data ?? []
    const hit = list.find(s => s.slug === 'verify-sample-skill')
    if (!hit) fail('E2E', `搜索结果不含 verify-sample-skill: ${JSON.stringify(list).slice(0, 300)}`)
    log('E2E', `搜到 skill id=${hit.id}, status=${hit.status}`, 'green')

    // ---- 步骤 8: 下载字节一致（sha256）----
    log('E2E', 'Step 8: 下载并校验 sha256', 'blue')
    const dlRes = await fetch(`${BACKEND_BASE}/api/skills/${hit.id}/download`)
    if (!dlRes.ok) fail('E2E', `下载失败 HTTP ${dlRes.status}`)
    const dlBuf = Buffer.from(await dlRes.arrayBuffer())
    const dlHash = createHash('sha256').update(dlBuf).digest('hex')
    const origBuf = readFileSync(zipPath)
    const origHash = createHash('sha256').update(origBuf).digest('hex')
    if (dlHash !== origHash) {
      fail('E2E', `sha256 不一致 orig=${origHash} dl=${dlHash}`)
    }
    log('E2E', `sha256 一致 (${dlHash.slice(0, 12)}...)`, 'green')

    log('E2E', '全部断言通过', 'green')
    return { skillId: hit.id, slug: hit.slug }
  } catch (e) {
    try { await page.screenshot({ path: path.join(SCREENSHOTS_DIR, '99-fail.png'), fullPage: true }) } catch { /* */ }
    throw e
  } finally {
    await ctx.close()
    await browser.close()
  }
}

// ====== 6. 收尾：清理 spawned 进程 ======
function killProc(proc, name) {
  if (!proc) return
  try {
    if (process.platform === 'win32') {
      spawnSync('taskkill', ['/pid', String(proc.pid), '/f', '/t'], { stdio: 'ignore' })
    } else {
      proc.kill('SIGTERM')
    }
    log('CLEANUP', `${name} 已终止 (pid=${proc.pid})`, 'dim')
  } catch (e) {
    log('CLEANUP', `${name} 终止失败: ${e.message}`, 'yellow')
  }
}

// ====== Main ======
(async () => {
  console.log(`${C.blue}🚀 S38 E2E verify starting...${C.reset}`)
  console.log(`${C.dim}   PROJECT_ROOT=${PROJECT_ROOT}${C.reset}`)
  let backend, frontend
  try {
    // ---- 阶段 1: 启后端 ----
    backend = await startBackend()

    // ---- 阶段 2: 拿 token ----
    const token = await loginViaApi()

    // ---- 阶段 3: 启前端 ----
    frontend = await startFrontend()

    // ---- 阶段 4: Playwright E2E ----
    const result = await runE2E(token)

    console.log()
    console.log(`${C.green}✅ S38 E2E PASS — skill id=${result.skillId}, slug=${result.slug}${C.reset}`)
    console.log(`${C.dim}   截图: ${SCREENSHOTS_DIR}/01-upload-success.png${C.reset}`)
    process.exit(0)
  } catch (e) {
    console.error()
    console.error(`${C.red}❌ S38 E2E FAIL: ${e.message}${C.reset}`)
    console.error(`${C.dim}   失败截图（如有）: ${SCREENSHOTS_DIR}/99-fail.png${C.reset}`)
    process.exit(1)
  } finally {
    killProc(backend, 'backend')
    killProc(frontend, 'frontend')
  }
})()
